import java.util.Scanner;
class AssignmentExample{
public static void main(String args[]){
Scanner sc = new Scanner(System.in);
System.out.println("Enter a value");
int a = sc.nextInt();
System.out.println(a+=10);
System.out.println(a-=10);
System.out.println(a*=10);
System.out.println(a/=10);
System.out.println(a%=10);
}
}

